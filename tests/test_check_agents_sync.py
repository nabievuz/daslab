# ---------------------------------------------------------------------------
# D11 — SKIP_KEYS are excluded from cross-check
# ---------------------------------------------------------------------------


def test_skip_keys_are_excluded(tmp_path: Path) -> None:
    """External-runtime pilots in SKIP_KEYS do not count as shim files."""
    agents_dir = tmp_path / ".claude" / "agents"
    agents_dir.mkdir(parents=True, exist_ok=True)

    # Write a shim for a SKIP key — it must be ignored by load_shims
    for skip_key in SKIP_KEYS:
        fm = make_shim(skip_key)
        write_shim_file(tmp_path, skip_key, fm)

    shims = load_shims(agents_dir)
    for skip_key in SKIP_KEYS:
        assert skip_key not in shims, f"SKIP key '{skip_key}' leaked into shims"


