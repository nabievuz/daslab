def test_all_valid_models_accepted() -> None:
    """Each of the four valid model tiers produces no model-tier error."""
    routing = make_routing_dict(["qa-eng"])
    for model in VALID_MODELS:
        shims = {"qa-eng": make_shim("qa-eng", model=model)}
        errors = check_sync(shims, routing, policy_models=None)
        tier_errors = [e for e in errors if "not a valid tier" in e]
        assert not tier_errors, f"model '{model}' unexpectedly flagged: {errors}"


