def test_main_no_strict_missing_policy(tmp_path: Path, capsys: pytest.CaptureFixture) -> None:
    """main() returns 0 and prints 'policy not yet in-tree' when policy absent."""
    for role in ["qa-eng", "cto", "backend-eng-1"]:
        write_shim_file(tmp_path, role, make_shim(role))
    routing_path = write_routing_file(tmp_path, _BASE_ROUTING)
    agents_dir = tmp_path / ".claude" / "agents"

