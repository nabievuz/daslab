# D12 — main() integration: in-sync tree exits 0, drifted tree exits 1
# ---------------------------------------------------------------------------


def test_main_in_sync(tmp_path: Path) -> None:
    """main() returns 0 when agents and ROUTING.md are in sync."""
    key = "qa-eng"
    write_shim_file(tmp_path, key, make_shim(key))
    routing_path = write_routing_file(
        tmp_path,
        _BASE_ROUTING,  # contains qa-eng, cto, backend-eng-1
    )
    # Write shims for all three roles in the routing table. Models must match
    # the real policy table (read by main() when no --policy is given): cto is
    # opus, backend-eng-1 is sonnet.
    write_shim_file(tmp_path, "cto", make_shim("cto", model="opus"))
    write_shim_file(tmp_path, "backend-eng-1", make_shim("backend-eng-1"))

    agents_dir = tmp_path / ".claude" / "agents"
    rc = main(
        [
            "--agents", str(agents_dir),
            "--routing", str(routing_path),
        ]
    )
    assert rc == 0, f"Expected exit 0 but got {rc}"


def test_main_drifted(tmp_path: Path) -> None:
    """main() returns 1 when a shim is missing from ROUTING.md."""
    write_shim_file(tmp_path, "orphan-role", make_shim("orphan-role"))
    routing_path = write_routing_file(tmp_path, _BASE_ROUTING)  # no orphan-role row
    agents_dir = tmp_path / ".claude" / "agents"
    rc = main(
        [
            "--agents", str(agents_dir),
            "--routing", str(routing_path),
        ]
    )
    assert rc == 1, f"Expected exit 1 (drift) but got {rc}"


