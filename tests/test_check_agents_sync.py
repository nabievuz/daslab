_BASE_ROUTING = textwrap.dedent(
    """\
    # Role routing — reviewer/manager per role

    > Generated by scripts/gen_subagents.py — do not edit by hand.

    | Role key | Display name | Dept | Reports to (reviewer) |
    |---|---|---|---|
    | `qa-eng` | QA Engineer | engineering | QA Lead |
    | `cto` | CTO | engineering | CEO |
    | `backend-eng-1` | Backend Engineer 1 | engineering | Backend EM |
    """
)

_KNOWN_ROLES = frozenset({"qa-eng", "cto", "backend-eng-1"})


def make_shim(key: str, model: str = "sonnet", name: str = "") -> dict[str, str]:
    """Return a minimal shim frontmatter dict."""
    return {
        "name": name or key,
        "model": model,
        "description": f"DasLab engineering role — {key}.",
    }


def make_routing_dict(keys: list[str]) -> dict[str, str]:
    """Return a routing dict ``{role_key: reports_to}``."""
    return dict.fromkeys(keys, "some-manager")


def write_shim_file(tmp_path: Path, key: str, fm: dict[str, str]) -> Path:
    """Write a ``.claude/agents/<key>.md`` shim file and return its path."""
    agents_dir = tmp_path / ".claude" / "agents"
    agents_dir.mkdir(parents=True, exist_ok=True)
    lines = ["---"]
    for k, v in fm.items():
        lines.append(f"{k}: {v}")
    lines += ["---", "", "You are a DasLab agent.", ""]
    path = agents_dir / f"{key}.md"
    path.write_text("\n".join(lines), encoding="utf-8")
    return path


def write_routing_file(tmp_path: Path, content: str) -> Path:
    """Write a ROUTING.md to *tmp_path*/board/ROUTING.md and return its path."""
    board_dir = tmp_path / "board"
    board_dir.mkdir(parents=True, exist_ok=True)
    path = board_dir / "ROUTING.md"
    path.write_text(content, encoding="utf-8")
    return path


def write_policy_file(tmp_path: Path, rows: dict[str, str]) -> Path:
    """Write a minimal model-allocation.md to *tmp_path*/governance/policies/."""
    policy_dir = tmp_path / "governance" / "policies"
    policy_dir.mkdir(parents=True, exist_ok=True)
    lines = ["# Model allocation", "", "| Role | Model | Rationale |", "|---|---|---|"]
    for key, model in rows.items():
        lines.append(f"| `{key}` | {model} | test |")
    path = policy_dir / "model-allocation.md"
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")
    return path


