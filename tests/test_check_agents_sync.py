def test_load_routing_parses_table(tmp_path: Path) -> None:
    """load_routing returns {role_key: reports_to} for each table row."""
    path = write_routing_file(tmp_path, _BASE_ROUTING)
    routing = load_routing(path)
    assert "qa-eng" in routing
    assert "cto" in routing
    assert "backend-eng-1" in routing
    assert "nonexistent" not in routing


def test_load_policy_models_parses_table(tmp_path: Path) -> None:
    """load_policy_models returns {role_key: model} for each table row."""
    policy_path = write_policy_file(tmp_path, {"qa-eng": "sonnet", "cto": "opus"})
    models = load_policy_models(policy_path)
    assert models is not None
    assert models["qa-eng"] == "sonnet"
    assert models["cto"] == "opus"


def test_load_policy_models_absent_returns_none(tmp_path: Path) -> None:
    """load_policy_models returns None when the file does not exist."""
    result = load_policy_models(tmp_path / "nonexistent.md")
    assert result is None


