"""tests/test_check_agents_sync.py — pytest suite for scripts/check_agents_sync.py.

All tests use synthetic in-memory or temporary-file fixtures — no real repo
files are read.  The fixture-based approach keeps the suite hermetic and fast:
it proves drift-detection logic independently of whatever state the real
worktree is in.

Coverage
--------
D1  shim key present in ROUTING.md but no shim file → drift detected.
D2  shim file exists but key absent from ROUTING.md → drift detected.
D3  shim ``name:`` field mismatches its filename stem → drift detected.
D4  shim ``model:`` field is not a valid tier → drift detected.
D5  shim ``model:`` is missing entirely → drift detected.
D6  shim model differs from policy table → drift detected.
D7  policy model matches shim → no drift.
D8  all in sync (no policy) → clean exit 0.
D9  all in sync (with policy) → clean exit 0.
D10 missing ``name:`` in shim → drift detected.
D11 SKIP_KEYS are excluded from cross-check.
D12 main() integration — in-sync fixture tree exits 0, drifted tree exits 1.
D13 main() with missing agents dir exits 2.
D14 main() with missing ROUTING.md exits 2.
D15 policy absent, --strict flag → exits 2.
D16 policy absent, no --strict → clean pass (prints policy not yet in-tree).
"""

