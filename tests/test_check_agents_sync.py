# load_shims — file-system round-trip
# ---------------------------------------------------------------------------


def test_load_shims_roundtrip(tmp_path: Path) -> None:
    """write a shim file, load it back, and verify frontmatter is intact."""
    fm = make_shim("qa-eng", model="sonnet")
    write_shim_file(tmp_path, "qa-eng", fm)
    agents_dir = tmp_path / ".claude" / "agents"
    loaded = load_shims(agents_dir)
    assert "qa-eng" in loaded
    assert loaded["qa-eng"]["name"] == "qa-eng"
    assert loaded["qa-eng"]["model"] == "sonnet"
