def test_routing_key_no_shim_detected() -> None:
    """Role in ROUTING.md with no matching shim file triggers drift."""
    shims: dict[str, dict[str, str]] = {}  # empty — no shims at all
    routing = make_routing_dict(["qa-eng"])
    errors = check_sync(shims, routing, policy_models=None)
    assert any("ROUTING.md" in e and "qa-eng" in e for e in errors), errors


